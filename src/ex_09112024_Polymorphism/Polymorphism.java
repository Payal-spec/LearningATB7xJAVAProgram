package ex_09112024_Polymorphism;

public class Polymorphism {

    int add ( int a, int b){
        return a+b;
    }

    double add ( double b, double a){
        return a+b;
    }

    String add (String a, String b, char c){
        return a+b+c;
    }

    float add (float a, float b){
        return a+b;
    }
}
